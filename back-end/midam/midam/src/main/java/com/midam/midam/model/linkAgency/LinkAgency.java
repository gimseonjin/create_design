package com.example.midam.vo.linkAgency;

public class LinkAgency {

    private String linkAgencyCode;
    private String regionCode;
    private String linkAgencyName;
    private String linkAgencyAddress;
    private String linkAgencyInfo;

    public String getLinkAgencyCode() {
        return linkAgencyCode;
    }

    public void setLinkAgencyCode(String linkAgencyCode) {
        this.linkAgencyCode = linkAgencyCode;
    }

    public String getRegionCode() {
        return regionCode;
    }

    public void setRegionCode(String regionCode) {
        this.regionCode = regionCode;
    }

    public String getLinkAgencyName() {
        return linkAgencyName;
    }

    public void setLinkAgencyName(String linkAgencyName) {
        this.linkAgencyName = linkAgencyName;
    }

    public String getLinkAgencyAddress() {
        return linkAgencyAddress;
    }

    public void setLinkAgencyAddress(String linkAgencyAddress) {
        this.linkAgencyAddress = linkAgencyAddress;
    }

    public String getLinkAgencyInfo() {
        return linkAgencyInfo;
    }

    public void setLinkAgencyInfo(String linkAgencyInfo) {
        this.linkAgencyInfo = linkAgencyInfo;
    }
}
